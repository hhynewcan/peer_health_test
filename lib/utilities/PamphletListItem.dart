// Utility list class for storing pamphlets
class PamphletListItem{

  String name;
  String address;
  String lightModeIconAddress;  // The resource address for light mode icon
  String darkModeIconAddress;   // The resource address for dark mode icon

  PamphletListItem(this.name, this.address, this.lightModeIconAddress, this.darkModeIconAddress);
}